async function renderServices() {
  const container = document.getElementById('services-grid');
  if (!container) return;

  try {
    const res      = await fetch('data/services.json');
    const services = await res.json();

    container.innerHTML = services.map(s => `
      <div class="service-card reveal">
        <div class="service-card-icon" aria-hidden="true">
          ${ICONS[s.icon] || ''}
        </div>
        <div>
          <h3 data-i18n="${s.title_key}"></h3>
          <p data-i18n="${s.desc_key}"></p>
        </div>
      </div>
    `).join('');
  } catch (err) {
    console.warn('[main] Could not load services.json', err);
  }
}

async function renderFooterSocial() {
  const container = document.getElementById('footer-social');
  if (!container) return;

  try {
    const res      = await fetch('data/contacts.json');
    const contacts = await res.json();

    container.innerHTML = Object.values(contacts).map(c => `
      <a href="${c.url}" aria-label="${c.label}" target="_blank" rel="noopener noreferrer">
        ${SOCIAL_ICONS[c.icon] || ''}
      </a>
    `).join('');
  } catch (err) {
    console.warn('[main] Could not load contacts.json', err);
  }
}

function updateCopyrightYear() {
  const el = document.getElementById('footer-year');
  if (el) el.textContent = new Date().getFullYear();
}

function initScrollAnimations() {
  const targets = document.querySelectorAll('.reveal, .stagger-children');
  if (!targets.length) return;

  const observer = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  targets.forEach(el => observer.observe(el));
}

async function main() {
  // 1. Apply theme immediately (before paint)
  initTheme();

  // 2. Render dynamic content
  await renderServices();
  renderFooterSocial();
  updateCopyrightYear();

  // 3. Apply i18n AFTER services rendered (so data-i18n attrs exist in DOM)
  await initI18n();

  // 4. Nav & contact
  initNav();
  initContact();

  // 5. Scroll animations
  initScrollAnimations();
}

main();